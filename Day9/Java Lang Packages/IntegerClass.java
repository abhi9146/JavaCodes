public class IntegerClass {
    public static void main(String[] args) {
        // Integer built-in methods
        System.out.println(Integer.toBinaryString(30));
        System.out.println(Integer.parseInt("45232"));
        System.out.println(Integer.bitCount(17));
        System.out.println(Integer.numberOfLeadingZeros(16));
        System.out.println(Integer.reverseBytes(1));
        System.out.println(Integer.MAX_VALUE);
        System.out.println(Integer.sum(10,20));
    }
}
